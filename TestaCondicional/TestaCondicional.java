public class TestaCondicional {
    public static void main(String[] args) {
        int idade = 21;
        if (idade >= 18) {
            System.out.println("Você já é maior de idade!");
            System.out.println("Seja bem-vindo!");
        }
        else {
            System.out.println("Infelizmente você não pode entrar! :(");
        }
    }
}